package com.wfairclough.foundation4gwt.client.ui.base;

import com.wfairclough.foundation4gwt.client.ui.constants.Radius;

/**
 * Interface for components that have a {@link Radius}
 * 
 * @since 1.0.0
 * 
 * @author Will Fairclough
 * 
 */
public interface HasRadius {

	/**
	 * Replaces the widet's radius style.
	 * 
	 * @param radius
	 *            the radius style to apply to the widget
	 */
	void setRadius(Radius radius);

}
